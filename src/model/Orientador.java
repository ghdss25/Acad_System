package model;

public class Orientador {
	
	private int ORIENT_ID;
	private String ORIENT_NOME;
	private java.sql.Date ORIENT_DATANASC;
	private String ORIENT_CPF;
	private String ORIENT_INSTITUICAO;
	private String ORIENT_TITULACAO;
	
	
	
	public int getORIENT_ID() {
		return ORIENT_ID;
	}
	public void setORIENT_ID(int ORIENT_ID) {
		this.ORIENT_ID = ORIENT_ID;
	}
	public String getORIENT_NOME() {
		return ORIENT_NOME;
	}
	public void setORIENT_NOME(String ORIENT_NOME) {
		this.ORIENT_NOME = ORIENT_NOME;
	}
	public java.sql.Date getORIENT_DATANASC() {
		return ORIENT_DATANASC;
	}
	public void setORIENT_DATANASC(java.sql.Date ORIENT_DATANASC) {
		this.ORIENT_DATANASC = ORIENT_DATANASC;
	}
	public String getORIENT_CPF() {
		return ORIENT_CPF;
	}
	public void setORIENT_CPF(String ORIENT_CPF) {
		this.ORIENT_CPF = ORIENT_CPF;
	}
	public String getORIENT_INSTITUICAO() {
		return ORIENT_INSTITUICAO;
	}
	public void setORIENT_INSTITUICAO(String ORIENT_INSTITUICAO) {
		this.ORIENT_INSTITUICAO = ORIENT_INSTITUICAO;
	}
	public String getORIENT_TITULACAO() {
		return ORIENT_TITULACAO;
	}
	public void setORIENT_TITULACAO(String ORIENT_TITULACAO) {
		this.ORIENT_TITULACAO = ORIENT_TITULACAO;
	}

}
