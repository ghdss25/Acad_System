package model;

public class Usuario {
	
	private int USU_ID;
	private String USU_NOME;
	private String USU_TEL;
	private String USU_SENHA;
	private String USU_TIPO;
	
	
	public int getUSU_ID() {
		return USU_ID;
	}
	public void setUSU_ID(int USU_ID) {
		this.USU_ID = USU_ID;
	}
	public String getUSU_NOME() {
		return USU_NOME;
	}
	public void setUSU_NOME(String USU_NOME) {
		this.USU_NOME = USU_NOME;
	}
	public String getUSU_TEL() {
		return USU_TEL;
	}
	public void setUSU_TEL(String USU_TEL) {
		this.USU_TEL = USU_TEL;
	}
	public String getUSU_SENHA() {
		return USU_SENHA;
	}
	public void setUSU_SENHA(String USU_SENHA) {
		this.USU_SENHA = USU_SENHA;
	}
	public String getUSU_TIPO() {
		return USU_TIPO;
	}
	public void setUSU_TIPO(String USU_TIPO) {
		this.USU_TIPO = USU_TIPO;
	}
	
}
